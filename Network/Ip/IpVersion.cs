
namespace ConsoleApplication36.Network.Ip {
	/// <summary>
	/// Represents the possible values of the version field of an
	/// IP packet.
	/// </summary>
	public enum IpVersion {
		/// <summary>
		/// IP version 4 (IPv4).
		/// </summary>
		Ipv4 = 0x04,
		/// <summary>
		/// IP version 6 (IPv6).
		/// </summary>
		Ipv6 = 0x06
	}
}
